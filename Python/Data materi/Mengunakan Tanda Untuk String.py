# menggunakan tanda \

# membuat tanda jadi string  \'
print('ain\'t dont do that')

# backslash \\
print('C:\\dokumen\\user')

# tab \t
print('Membuat teks jadi \t pisah')

# Backspaces \b
print('Membuat teks jadi \bgaada jarak')

#paragraf baru (newline) \n atau \r atau \r\n
print('ini paragraf pertama \nini paragraf ke dua')
print('ini paragraf pertama \rini paragraf ke dua')
print('ini paragraf pertama \r\nini paragraf ke dua')
 
# Menggunakan raw string (r'') atau (r"")
# ini akan membuat semua tanda menjadi string
print(r'\rfbejfjd\nbshdhe\m\s\f\b\j')

# mengunakan literal string """
print("""
nama : topiyobi
skeefhfhfj
shfusffhsufh
""")

# gabungan literal dan raw string (r""" """)
print(r"""
nama ;\saya
tidak\tahu \bodoamat
""")